package model;

public class TotalVendas {

    private String dataVenda;
    private int quantidadeDataVenda;

    public String getDataVenda() {
        return dataVenda;
    }

    public void setDataVenda(String dataVenda) {
        this.dataVenda = dataVenda;
    }

    public int getQuantidadeDataVenda() {
        return quantidadeDataVenda;
    }

    public void setQuantidadeDataVenda(int quantidadeDataVenda) {
        this.quantidadeDataVenda = quantidadeDataVenda;
    }

}